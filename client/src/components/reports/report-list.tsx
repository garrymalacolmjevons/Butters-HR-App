type ExportRecordWithUserName = ExportRecord & { userName: string };

export function RecentExports() {
  const { toast } = useToast();
  const { data: exports = [], isLoading } = useQuery<ExportRecordWithUserName[]>({
    queryKey: ["/api/export-records"],
  });

  const handleDownload = (exportRecord: ExportRecordWithUserName) => {
    toast({
      title: "Download requested",
      description: "The export is being generated and will download shortly.",
    });
    
    // This would need to be updated with actual download functionality
    // For now, we'll just simulate a download
    setTimeout(() => {
      toast({
        title: "Download complete",
        description: `${exportRecord.reportName} has been downloaded.`,
      });
    }, 2000);
  };

  if (isLoading) {
    return (
      <Card>
        <CardHeader>
          <CardTitle>Recent Exports</CardTitle>
        </CardHeader>
        <CardContent>
          <div className="animate-pulse space-y-4">
            {[...Array(5)].map((_, i) => (
              <div key={i} className="h-12 bg-neutral-200 rounded"></div>
            ))}
          </div>
        </CardContent>
      </Card>
    );
  }

  const getFileIcon = (format: string) => {
    switch (format.toLowerCase()) {
      case "xlsx":
        return <span className="text-green-600 mr-2">ðŸ“Š</span>;
      case "csv":
        return <span className="text-indigo-600 mr-2">ðŸ“ˆ</span>;
      case "pdf":
        return <span className="text-red-600 mr-2">ðŸ“„</span>;
      default:
        return <span className="text-neutral-600 mr-2">ðŸ“‘</span>;
    }
  };

  return (
    <Card>
      <CardHeader>
        <CardTitle>Recent Exports</CardTitle>
      </CardHeader>
      <CardContent>
        <table className="w-full">
          <thead>
            <tr className="border-b border-neutral-200">
              <th className="text-left py-3 px-4 text-sm font-medium text-neutral-700">Report</th>
              <th className="text-left py-3 px-4 text-sm font-medium text-neutral-700">Date</th>
              <th className="text-left py-3 px-4 text-sm font-medium text-neutral-700">Company</th>
              <th className="text-left py-3 px-4 text-sm font-medium text-neutral-700">Generated By</th>
              <th className="text-left py-3 px-4 text-sm font-medium text-neutral-700">Actions</th>
            </tr>
          </thead>
          <tbody>
            {exports.length === 0 ? (
              <tr>
                <td colSpan={5} className="py-4 text-center text-neutral-500">
                  No recent exports found
                </td>
              </tr>
            ) : (
              exports.map((exportRecord) => (
                <tr key={exportRecord.id} className="border-b border-neutral-200">
                  <td className="py-3 px-4">
                    <div className="flex items-center">
                      {getFileIcon(exportRecord.format || "xlsx")}
                      <span>{exportRecord.reportName}</span>
                    </div>
                  </td>
                  <td className="py-3 px-4">{formatDateTime(exportRecord.createdAt)}</td>
                  <td className="py-3 px-4">{exportRecord.company || "All Companies"}</td>
                  <td className="py-3 px-4">{exportRecord.userName}</td>
                  <td className="py-3 px-4">
                    <div className="flex space-x-2">
                      <Button 
                        size="icon" 
                        variant="ghost" 
                        onClick={() => handleDownload(exportRecord)}
                      >
                        <Download className="h-4 w-4 text-primary" />
                      </Button>
                      <Button size="icon" variant="ghost">
                        <History className="h-4 w-4 text-neutral-500" />
                      </Button>
                    </div>
                  </td>
                </tr>
              ))
            )}
          </tbody>
        </table>
      </CardContent>
    </Card>
  );
}

